const mongoose = require('mongoose');

const userSchema = new mongoose.Schema(
   {
      fullName: {
         type: String,
         required: true,
         trim: true,
      },
      email: {
         type: String,
         required: true,
         unique: true,
         lowercase: true,
      },
      password: {
         type: String,
         required: true,
      },
      role: {
         type: String,
         enum: ["citizen", "admin"],
         default: "citizen",
      },
      photo: {
         type: String,
      },
   },
   {
      timestamps: true,
      collection: 'users'
   }
);

module.exports = mongoose.model('User', userSchema);
